		function showup(){
			$('.popup').css('display','block');
			$('.icon-comment').css('display','none');
			$('.icon-remove').css('display','block');
		}
			
		function flyout(){
			$('.popup').css('display','none');
			$('.icon-comment').css('display','block');
			$('.icon-remove').css('display','none');
		}